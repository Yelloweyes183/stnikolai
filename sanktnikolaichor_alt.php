Herunter von der Autobahn und immer Richtung Göteborg – wer so fährt, kommt nicht nur zur „Kieler Woche“, sondern auch zur St. Nikolaikirche Kiel, in der schon vor mehr als 80 Jahren ein Oratorienchor gegründet wurde.<br>
Der umfaßte damals 200 Sänger. Heute hat der SanktNikolaiChor 50 Mitglieder, die auf hohem Niveau die Doppelaufgabe eines Konzert- und Gemeindechores erfüllen. Die schlanke Besetzung ermöglicht in ihrer Flexibilität hochkarätige Projekte. Fast alle Oratorien vom Barock bis in die Moderne gehören zu seinem Repertoire, dessen größere Besetzungen gemeinsam mit befreundeten Chören realisiert werden. Aber auch die anspruchsvollen a-cappella-Werke aller Epochen werden regelmäßig zur Aufführung gebracht. Über die Grenzen von Schleswig-Holstein hinaus zählt der SanktNikolaiChor zu den führenden Oratorienchören Deutschlands. Er ist auch im Ausland (Italien, Schweiz, Frankreich, Polen und England) unterwegs. Er war Preisträger beim Schleswig-Holsteinischen Chorwettbewerb.<br>
2001 gestaltete das Ensemble in der Bristol Cathedral eine Woche lang die täglichen Evensongs und Gottesdienste und brachte zudem das „Nunc dimittis“ zur Uraufführung, das Chorleiter Rainer-Michael Munz für das Domkapitel von Bristol komponierte. Der erfolgreichen Tätigkeit in Bristol folgten weitere Einladungen als begehrter Gastchor an renommierte englische Kathedralen (u.a. Canterbury, York, Lincoln, Salisbury und natürlich auch gerne wieder Bristol).<br>
2003 wurde die Einladung nach York angenommen. Auch dort versah der SanktNikolaiChor für eine Woche den umfangreichen täglichen Chordienst in der dortigen Kathedrale.

