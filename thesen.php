## ...zur Kirchenpädagogik

"Bundesverband Kirchenpädagogik e.V."
 
Kirchenpädagogik will Kirchenräume für Menschen öffnen und den Sinngehalt christlicher Kirchen mit Kopf, Herz und Hand erschließen und vermitteln, um so Inhalte des christlichen Glaubens bekannt zu machen und einen Zugang zu spirituellen Dimensionen zu ermöglichen. Angesichts dieser Aufgabe tritt das jeweils unterschiedlich ausgeprägte Selbstverständnis der Konfessionen hinter den grundsätzlichen Gemeinsamkeiten zurück.
 
1. Kirchenpädagogik bringt Mensch und Kirchenraum in Beziehung.
Kirchenräume mit ihren in Architektur und Ausstattung bewahrten christlichen Glaubensaussagen und Traditionen können neue Bedeutung gewinnen, indem sie mit dem Lebenshorizont der beteiligten Menschen in Beziehung gesetzt werden.
Kirchenpädagogik nimmt hierbei die Vorerfahrungen und Empfindungen der Teilnehmenden ernst und bezieht deren fremden Blick mit ein.

2. Kirchenpädagogik bedeutet raum- und erfahrungsbezogenes Arbeiten.
Kirchenräume sind Ort, Gegenstand und Medium der Kirchenpädagogik. Räume machen die eigene Leiblichkeit bewußt; sie werden mit dem ganzen Körper und mit allen Sinnen erfahren.
Kirchenpädagogik erschließt Kirchenräume nicht nur sprachlich und visuell, sondern auch im Durchschreiten, Ertasten, Empfinden.

3. Kirchenpädagogik eröffnet Zugänge zu religiösen Erfahrungen.
Die besondere Ausstrahlung des Raumes sowie die persönliche Ansprache, die Konzentration der Wahrnehmung und die Verlangsamung des Alltagstempos in der kirchenpädagogischen Arbeit können Zugänge zu oftmals verschütteten religiösen Erfahrungen und Sehnsüchten der Beteiligten anbahnen.
Kirchenpädagogik hat die Aufgabe, diesen Prozeß unaufdringlich und behutsam zu moderieren.

4. Kirchenpädagogik arbeitet in methodischer Vielfalt.
Kirchenpädagogik greift ästhetische, dramaturgische, körperbezogene, musikalische und meditative Vermittlungsansätze so wie klassische Methoden der Religionspädagogik auf. Ihre Auswahl ist abhängig von der Zielgruppe, den thematischen Anknüpfungen im Kirchenraum und den örtlichen Rahmenbedingungen.
Die Entwicklung methodischer Grundlinien aus den unterschiedlichen Ansätzen und Erfahrungen steht für die nächsten Jahre an.

5. Kirchenpädagogik braucht Zeit.
Das Lernen im Kirchenraum bedarf einer Verlangsamung, um Wahrnehmungsprozessen Raum zu geben und für Achtsamkeitserfahrungen Zeit zu lassen.
Wer an einem kirchenpädagogischen Projekt beteiligt ist, nimmt sich Zeit. Für Schulklassen haben sich mehrere Stunden bewährt.

6. Kirchenpädagogik wirkt nach außen.
Kirchenpädagogik ist im Zusammenspiel von religions- und museumspädagogischer Bildungsarbeit mit Kindern und Jugendlichen entwickelt worden und hat hierin ihr ursprüngliches Aufgabenfeld. Sie verknüpft Inhalte des Religionsunterrichts mit den Fragen vieler Schulfächer, insbesondere des Geschichts-, Sachkunde-, Kunst-, Politik-, Latein- und Musikunterrichts. Die schulische Verfächerung wird am authentischen Ort christlicher Überlieferung und gelebter Praxis aufgebrochen. Den Schulen eröffnet sie einen außerschulischen Lernort und wirkt ihrerseits auf die innerschulische Bildungsarbeit ein.
Kirchenführungen für Touristen erhalten neue Impulse, wenn sie sich auf die Moderation des Dialogs zwischen den Menschen und dem Kirchenraum einlassen.

7. Kirchenpädagogik wirkt nach innen.
Kirchenpädagogik regt die gemeindepädagogische Arbeit mit Kindern, Jugendlichen und Erwachsenen an und verhilft durch die Erschließung des Kirchenraumes zu einer persönlichen Verwurzelung und Standortbestimmung. Sie macht die Stellung des Raumes im Alltag und in der gottesdienstlichen Feier der Gemeinde bewußt.
Der Kirchenraum, seine Gestaltung, Betreuung und Vermittlung nach außen kann sich zu einer gemeindlichen Mitte entwickeln. Auch Menschen außerhalb traditioneller Formen der Gemeindearbeit lassen sich in dieses Aufgabengebiet einbinden.

8. Kirchenpädagogik ist eine langfristige Investition in die kommende Generation.
Die Zukunft der Kirche in der multikulturellen Gesellschaft hängt nicht unerheblich davon ab, ob den Menschen säkularisierter und anderer kultureller Kontexte christliche Inhalte verständlich und zugänglich gemacht werden können.
Als ein Projekt der Übersetzung an der Schwelle zwischen Kirche und Gesellschaft leistet die Kirchenpädagogik für die Begegnung mit der biblischen Botschaft einen unverzichtbaren Beitrag. Dieser Stellenwert muss sowohl Kirchengemeinden wie Landeskirchen und Bistümern in nächster Zeit vermittelt werden, da Kirchenpädagogik nicht zuletzt auf konkrete Unterstützung vor Ort angewiesen ist!

im September 2002 auf der Bundesversammlung in Osnabrück verabschiedet


