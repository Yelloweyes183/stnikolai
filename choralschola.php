# Choralschola St. Nikolai

<hr>

<img src="/img/Choralschola St. Nikolai Kiel.jpg" alt="Foto der Choralschola" class="r">
<p>
  Die Choralschola St. Nikolai wirkte von 1989 bis 2022 an St. Nikolai und bestand aus ca. 15 Sängerinnen und Sängern. Gregorianischer Choral kann sein Wesen letztlich nur im Rahmen des Gottesdienstes voll entfalten. Daher betrachtete die Choralschola als ihre wichtigste Aufgabe die musikalische Gestaltung von Gottesdiensten an St. Nikolai, insbesondere der Abendgottesdienste, aber auch die Gestaltung von Gottesdiensten an den Hochfesten des Kirchenjahres, wie am Heiligen Abend, an Epiphanias und in der Osternacht. Viele Jahre lang gab die Choralschola am Samstag vor dem ersten Advent ein Konzert, in dem sie die adventlichen Gesänge der Gregorianik zum Erklingen brachte.<br>
  <br>
Die Leitung hatte der international renommierte Gregorianik-Experte Prof. Dr. Johannes Berchmans Göschl. Prof. Göschl hat dieses wichtige Amt aus Altersgründen nun niedergelegt, und da die Suche nach einer/einem geeigneten Nachfolger/in leider ergebnislos verlief, hat sich die Choralschola letztendlich aufgelöst. <br>
<br>
Eine lange gemeinsame Geschichte geht damit zu Ende – für St. Nikolai ist das ein echter Einschnitt! Der Kirchengemeinderat und die Kirchengemeinde von St. Nikolai sind Prof. Göschl und den Sängerinnen und Sängern der Choralschola sehr dankbar für die Pflege des Gregorianischen Chorals in St. Nikolai, die verlässliche und besondere Ausgestaltung der Gottesdienste und Konzerte in St. Nikolai und das gute Miteinander über diese lange Zeit.<br>
<br>
Gott befohlen! <br>
<br>
<i>KMD Volkmar Zehner</i>
</p>

