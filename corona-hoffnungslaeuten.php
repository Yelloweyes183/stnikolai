## Gebetsvorschläge

### GEBET 1
__Hoffnungsläuten__

Den Klang hören und durchatmen.<br>
Mit Gott sprechen. Vielleicht so:<br> 

Gott, sei bei mir.<br>
Höre meine Sorgen.<br>
[Stille]<br>
Höre meine Bitten:<br>
Für die Kranken. <br>
Für ihre Angehörige.<br>
Für die Pflegenden.<br>
Für die, denen das Geld ausgeht.<br>
Für die Erschöpften.<br>
Für die Sterbenden und die Trauernden.<br>
Tröste, trage, liebe alle, die dich brauchen. <br>
[Stille]

[Vaterunser]

Gott sagt: <br>
Fürchte dich nicht. Ich bin bei dir.<br>
Lass mich darauf vertrauen. <br>
Danke für alles, was stärkt.<br>
Amen. <br>


### GEBET 2 [für Menschen ohne christlichen Hintergrund]
__Hoffnungsläuten__

Es ist Zeit, eine kurze Pause einzulegen.<br>
Ich denke an Menschen, die es schwer haben. <br>
Ich erinnere mich an alles, was mich tröstet und stärkt.<br>
Ich halte einen guten Gedanken fest.<br>
Ich verbinde mich mit Menschen, die gerade auch die Glocken hören. <br>
Ich bin nicht allein. <br>

### GEBET 3
__Hoffnungsläuten__

Es ist Zeit innezuhalten, durchzuatmen, zu beten.<br>
Wir bringen vor Gott, was uns bewegt.<br>
Wir bitten für die Menschen, die jetzt für uns ihren Dienst tun.<br>
Wir bitten für die Kranken.<br>
Wir bitten um Hoffnung und Herzenskraft.<br>
Wir sind verbunden mit allen.<br>
Gott hat uns nicht gegeben den Geist der Furcht, sondern der Kraft und der Liebe und der Besonnenheit.<br>
Amen


### GEBET 4
__Hoffnungsläuten__

Hören Sie auf den Klang<br>
Atmen Sie tief durch. <br>
Schließen Sie die Augen,<br>
vielleicht legen Sie sich die Hand aufs Herz. <br>
Denken Sie an die Menschen, die es jetzt besonders brauchen.<br>
Schweigen, auf die Glocken hören<br>
Beten Sie ein Vaterunser<br>
Von guten Mächten wunderbar geborgen,<br>
erwarten wir getrost, was kommen mag. <br>
Gott ist bei uns am Abend und am Morgen, <br>
und ganz gewiss an jedem neuen Tag.<br>
Amen


