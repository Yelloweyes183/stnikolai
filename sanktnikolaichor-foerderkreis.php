# Förderkreis des SanktNikolaiChors Kiel e. V.:<br>„Zur Recreation des Gemüths“

<hr>

Musik solle „zu Gottes Ehre und zur Recreation des Gemüths seyn“. Diese Worte werden Johann Sebastian Bach zugeschrieben. Und Martin Luther meinte ungefähr 200 Jahre vorher, „nach der Theologie“ sei die Musik „die höchste Kunst“, wirke sie doch unmittelbar auf die Seele des Zuhörers.
 
Der <a href="/sanktnikolaichor">SanktNikolaiChor Kiel</a> sieht sich dieser Tradition verpflichtet und bereichert seit Jahrzehnten das Musikleben in Kiel mit hochkarätigen Gottesdienstmusiken und Konzerten. „Hochkarätig“ heißt für Konzerte aber auch „kostenträchtig“. So unwahrscheinlich es klingt: Selbst ein ausverkauftes Konzert deckt in der Regel nicht die Kosten. 
Der Förderkreis des SanktNikolaiChors gibt Zuschüsse zur Finanzierung der Konzerte, damit große und kleine bekannte sowie weniger bekannte Meisterwerke auch weiterhin in hoher Qualität zur Aufführung gelangen können. Außerdem ermöglicht der Förderkreis die Erstellung von Informationsmaterial über musikalische Veranstaltungen an St. Nikolai und gibt gelegentlich kleinere Finanzhilfen bei Konzertreisen des Chores.
 
Mit Ihrer Mitgliedschaft im Förderkreis unterstützen Sie die Arbeit des SanktNikolaiChores materiell und ideell. 
Wenn Sie uns ohne Vereinsmitgliedschaft regelmäßig oder einmalig Spenden zukommen lassen möchten, freuen wir uns natürlich auch.
 
In jedem Fall werden Sie, wenn Sie dies wünschen, regelmäßig über unsere kirchenmusikalischen Veranstaltungen informiert.

