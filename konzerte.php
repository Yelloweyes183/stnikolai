# Zum Tode von Hans Gebhard
Der langjährige Kantor und Organist an St. Nikolai starb am 5. November 2022.<br>
<br>
Hans Gebhard begann seinen Dienst an St. Nikolai im April 1959 und wurde von dieser Stelle im Januar 1989 verabschiedet. Dazwischen lagen arbeitsreiche und erfolgreiche Jahre als Leiter des SanktNikolaiChors  und als Organist. Sein breit gefächertes Wissen und sein Interesse ermöglichten Einstudierungen von Werken des Frühbarock bis zur Moderne, von Monteverdis <i>Marienvesper</i> über die Oratorien Bachs und Werke der Romantik bis zu Brittens <i>War Requiem</i> und Pendereckis <i>Lukas-Passion</i>.<br>
<br>
Von seinem Chor verlangte er viel, von sich selbst auch. Vorbereitungen für Konzerte in St. Nikolai, für Tonaufnahmen und Konzertreisen, ob mit dem Chor oder als Solist, waren intensiv, zeitaufwendig und kräftezehrend – und die Ergebnisse schenkten unzähligen Menschen, Mitwirkenden wie Zuhörenden, musikalische Erlebnisse von hoher Qualität. Aufführungen von Neuentdeckungen aus der zweiten Hälfte des 20. Jahrhunderts füllten die St. Nikolaikirche und den Konzertsaal im Schloss, aber auch das „Wagnis“ (so wurde es damals beschämenderweise tatsächlich noch empfunden), Mendelssohns <i>Elias</i> in den 70er Jahren erstmals nach dem Krieg wieder in Kiel aufzuführen, erregten Aufsehen und waren große Erfolge.<br>
<br>
1976 übernahm er an der Lübecker Musikhochschule eine Professur für Orgel und Chorleitung und reduzierte seine Tätigkeit an St. Nikolai, was aber nur bedeutete, dass er weniger Orgeldienste wahrnahm, die Chorleitung aber weiter in seinen Händen lag.<br>
<br>
Auch nach seiner Hochschultätigkeit war für ihn ein Leben ohne praktische kirchenmusikalische Arbeit nicht vorstellbar, und so fand er an St. Petri in Hamburg, seinem Wohnort, eine neue musikalische Heimat. Dort wurde am 19. November der Trauergottesdienst gehalten, nachdem er im Alter von 93 Jahren friedlich gestorben war.<br>
