<!--# Johann Sebastian Bach – das Gesamtwerk für Orgel

<div style="float: right">

  <img src="/img/grosse_orgel.jpg" style="max-width: 300px; padding-left: 24px;"> <br>

  </div>
<div style="text-align: justify;">
„Kein anderer Komponist hat ein Orgelwerk hinterlassen, das dem Bachschen an Umfang und Gewicht gleichkäme. Und dabei repräsentiert Bachs Orgelwerk nur einen Teil seines Lebens als Musiker und Komponist. Denn als Musiker seiner Zeit hat Bach natürlich dann viel für Orgel geschrieben, wenn er ein Amt als Organist innehatte: die Jahre, während derer er als Organist in Arnstadt, Mühlhausen und Weimar wirkte, waren für die Orgelkomposition die fruchtbarsten. Als Hofkomponist in Köthen und als Thomaskantor in Leipzig gehörte Orgelspiel nicht zu Bachs Dienstpflichten, darum entstanden dort vergleichsweise wenige Orgelwerke. Als Bach dann in den letzten 15 Jahren seines Musikerlebens – jenseits musikalischer Tagespflichten – exemplarische Werke zu den wichtigen Kunstgattungen seiner Epoche schuf, erlangte das Komponieren für Orgel wieder Bedeutung. In seinem Orgelwerk sehen wir Bach als Lernenden und Lehrenden; als einen, der für seine tägliche gottesdienstliche Praxis stets höchste Qualität lieferte; als einen, der sich mit glanzvollen Werken als Orgelvirtuose ohne Konkurrenz bewies; schließlich aber auch als den, der die Wurzel der Musik im Zusammenhang eines göttlichen Kosmos begriff, der sich in Kunst, Wissenschaft und Theologie gleichrangig äußerte“ (Martin Lücker).<br>
<br>
Die Kompositionen für Orgel von Johann Sebastian Bach beschäftigen mich seit meinen Anfängen als Orgelschüler in meiner Heimatstadt Emmendingen, nun seit insgesamt über 40 Jahren. Nach Jahren des Studierens in Hannover, Lyon und Hamburg und als Organist in Hamburg-Volksdorf entschloss ich mich vor drei Jahren, die noch nicht geübten Kompositionen Bachs gezielt zu lernen und den Konzertzyklus zu planen. Nun ist es soweit: die Konzertreihe mit dem Gesamtwerk für Orgel von Johann Sebastian Bach hat begonnen!<br>
Der Zyklus mit 19 Konzerten ist chronologisch aufgebaut: von den Anfängen in Ohrdruf und Lüneburg über Arnstadt, Mühlhausen und Weimar bis hin zu seiner letzten Stelle in Leipzig. Der <i>III. Theil der Clavierübung</i> (1739) bildete am Reformationstag 2020 den Auftakt; und die beiden Silvesterkonzerte 2021 und 2022 springen mit ihrem festlichen Programm (Toccaten und Fugen sowie die Konzertbearbeitungen) aus der chronologischen Reihung heraus.<br>
Die Corona-Pandemie hat den Zyklus zweimal unterbrochen; nun hoffe ich, dass die restlichen Konzerte wie geplant stattfinden können.
