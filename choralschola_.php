# Choralschola St. Nikolai

<hr>

<img src="/img/Choralschola St. Nikolai Kiel.jpg" alt="Foto der Choralschola" class="r">

 Die Choralschola St. Nikolai wurde von Bertold Höcker 1989 mit dem Ziel gegründet, authentische Gregorianik wieder zu pflegen. Zur Zeit besteht die Gruppe aus sechs Sängerinnen und Sängern. Sie sind der Überzeugung, erst durch Rückgriff auf die ältesten Quellen der gregorianischen Überlieferung des 8. und 9. Jahrhunderts die spirituelle Qualität des Chorals neu zu erschließen.

„Die Urquelle, aus der die gregorianischen Melodien entspringen und gespeist sind, ist das Wort. Das Wort der Liturgie, letztlich immer Anrede Gottes an den Menschen oder Hinwendung des Menschen zu Gott, findet seine höchste Erhöhung, wenn es in Musik erblühen kann. In vollendeter Weise geschieht dies im Gregorianischen Choral.“ (zitiert nach L. Agustoni und J. Göschl, Einführung in die Interpretation des Gregorianischen Chorals).

Gregorianischer Choral kann sein Wesen letztlich nur im Rahmen des Gottesdienstes voll entfalten. Die Konzerte der Choralschola St. Nikolai haben darum immer gottesdienstlichen Charakter. Die jeweilige Festzeit im Kirchenjahr mit ihrer besonderen Liturgie bestimmt Auswahl und Reihenfolge der Gesänge innerhalb eines Programms.

Die Einstimmigkeit des Gregorianischen Chorals ermöglicht den Hörenden, im gesungenen Wort den Kirchenraum mit seiner spirituellen Ausstrahlung auf besondere Weise wahrzunehmen. Vor allem romanische Kirchen, die für diese Gesänge gebaut wurden, scheinen „mitzusingen“.

Um diese Dimension auch bei der Probenarbeit nicht vermissen zu müssen, finden die Proben in der Heimatkirche der Choralschola, der offenen Kirche St. Nikolai am Alten Markt statt. Sie beginnen mit einer professionellen Atem- und Stimmbildung. Klang- und Vokalstudien haben zum Ziel, sich der Einstimmigkeit bis in die Obertöne hinein immer mehr anzunähern.

Zur Zeit nimmt die Choralschola St. Nikolai neue Mitglieder auf. Eine Einführung in mittelalterliche Spiritualität und die besondere Notation der Gregorianik ist selbstverständlich. Voraussetzung für die Mitarbeit in Gottesdienst und Konzert ist eine sichere Stimme und der Wunsch, sich musikalisch und zugleich geistlich zu betätigen.

Die nicht öffentlichen Proben finden wöchentlich freitags von 19.00 Uhr bis ca. 21.00 Uhr in der Kirche St. Nikolai am Alten Markt statt.

