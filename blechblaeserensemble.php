Das Kieler Blechbläserensemble (KBBE) ist seit 2007 in der Kieler St-Nikolai-Kirche beheimatet.  In ihm treffen sich bei wöchentlichem Probenbetrieb rund ein Dutzend Musiker, die Interesse an anspruchsvoller Blechbläser-Kammermusik haben. Die Mitglieder sind weit vernetzt. Das KBBE stellt seit seiner Gründung die Besetzung des hohen und tiefen Blechs im collegium musicum der Christian-Albrechts-Universität zusammen. Aber auch in Posaunenchören, dem Landesjugend- und anderen Orchestern sowie Bands sieht man das KBBE und seine Mitwirkenden regelmäßig.

Gegründet und geleitet wird das Ensemble von Frank Hennig, im Hauptberuf Jurist. Er unterrichtete viele Jahre Bläserklassen und Posaunenschüler und hat Ende 2015 den ersten C-Kurs für Posaunenchorleitung der Nordkirche mit Auszeichnung absolviert. Sönke Grunau ist sein versierter und erfahrener Co-Dirigent.

Die weiteren Mitglieder sind Laien, immer wieder wirken aber auch Bläser mit, die ihr Instrument studiert haben und in diesem Beruf tätig sind. Altersmäßig ist die Gruppe über Schüler und Studenten bis zu Berufstätigen strukturiert. Etliche Mitglieder haben über das KBBE ihren Weg in das Musikstudium genommen.

Über seine Hauptwirkungsstätte in St. Nikolai hinaus ist das KBBE im ganzen Lande und darüber hinaus aktiv: Bei Festakten der Universität genauso wie bei großen Festgottesdiensten der Nordkirche (Amtseinführungen des Bischofs, Rundfunkgottesdienste, größere Orgeleinweihungen). Zwei Auslandsreisen führten die Gruppe nach Polen (Posznán) und Kopenhagen. In Schleswig-Holstein musiziert das KBBE regelmäßig  u.a. in Schleswiger St.-Petri-Dom und in Flensburg (St. Nikolai) und hat mit namhaften Organisten und Chorleitern gearbeitet (Michael Mages, Rainer Selle, Henrich Schwerk, Bernhard Emmer, Eckhard Broxtermann, Mark Baumann).

Repertoiremäßig bildet das KBBE fast alles ab, was für Blechbläser geschrieben und bearbeitet wurde, von doppel- und dreichörigen venezianischen Musiken Gabrielis über die Epoche der Romantik mit groß angelegten motettischen Chorsätzen (F. Mendelssohn Bartholdy) bis hin zu Originalkompositionen und Bearbeitungen für Blech und Orgel (Richard Strauss, Eugène Gigout). Auch im gottesdienstlichen Bereich ist das KBBE bestrebt, die Vielfalt seines Repertoires zu Gehör zu bringen und bietet stets mehr als den vierstimmigen Standard-Choralsatz an.

Neue Mitglieder, die sich in diesem Metier bewegen wollen, sind dem KBBE stets willkommen und können sich an seinen Leiter wenden oder zu einer donnerstäglichen Probe einfinden im Gemeindehaus der Michaelis-Kirchengemeinde, Wulfsbrook 27, 24113 Kiel. Ausführliche Informationen finden Sie im Internet unter

