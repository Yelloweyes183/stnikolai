## Hauptorgel

<div class="zoomable" style="max-width: 400px">
<img class="r" src="/img/Hauptorgel.jpg">
</div>

Im Jahre 1965 entstand, gebaut von Detlef Kleuker (Brackwede), die heutige Orgel mit drei Manualen und Pedal mit 45 Registern (Hauptwerk, Schwellwerk, Rückpositiv, Pedal). Ihre Charakteristika waren Schleifladen, Normalkoppeln, eine mechanische Tontraktur, eine elektrische Registertraktur, elektrische Koppeln und 4 freie Kombinationen. Die Windladen wurden nicht aus Holz, sondern aus Kunststoff gefertigt.

Abnutzung und Verschleiß führten im Laufe der Jahre zu immer größeren Problemen. Die Mechanik wurde immer schwergängiger. Deshalb mußte der Organist einen immer größeren Tastendruck ausüben. Die Spannung, mit der die elektrische Anlage betrieben wurde, war zu hoch und nicht mehr zulässig. Es bestand Brandgefahr. Die Tastenbeläge lösten sich, die Registerwippen brachen ab. Die großen Prospektpfeifen sackten in den Füßen ein und drohten, in das Kirchenschiff zu stürzen.

Eine Generalüberholung mit Umbau wurde im Zeitraum Pfingsten 1996 bis Ostern 1997 von der Orgelbaufirma Ulrich Babel (Gettorf) vorgenommen. Die Orgel wurde bis auf das Gehäuse und das eiserne Ständerwerk (das Skelett der Orgel) ausgebaut. Die Windladen wurden zerlegt und gereinigt, und Elektromagnete wurden eingebaut. Alle Pfeifen wurden gereinigt und überarbeitet. Die Prospektpfeifen erhielten neue Füße mit Innenfüßen und Halterungen zur Gewichtsentlastung. Die Prospektpfeifen von Hauptwerk und Pedal wurden mit Silberbronze lackiert. Der akustische Untersatz 32' wurde in der tiefen Oktave voll ausgebaut. Die größten sechs der neuen bis zu 5  m langen Pfeifen stehen links und rechts neben dem Gehäuse. Durch die Trennung des Prinzipalpiffaro 8'+4' hat die Orgel jetzt 46 Register. Dazu benötigte man eine neue Schleife und eine zusätzliche Windlade für die Zungen im Schwellwerk.  Der Dulzian 16' und das Krummhorn 8' erhielten aus klanglichen Gründen neu mensurierte Becher. Das Gebläse, das jetzt nur noch Hauptwerk und Pedal mit Wind versorgt, wurde mit einem zusätzlichen Balg unter dem Gestühl Orgelempore neu eingebaut. Der Kanal zur Orgel wurde unter den Fußbodendielen verlegt.
Wegen der neuen zusätzlichen Koppeln wurde ein zweites Gebläse mit einem Balg für Schwellwerk und Rückpositiv nötig. Es steht unter der Schwellwerkslade. Der Winddruck wurde für alle Werke erhöht.

Die Orgel ist nach dem Umbau innen sehr gut zugänglich. Stimmen und Wartungsaufgaben können nun wesentlich einfacher durchgeführt werden. Die Orgel erhielt eine elektrische Tontraktur und auch eine elektrische Registertraktur. Ein elektronischer Setzer und eine Selbstspieleinrichtung wurden angeschlossen. Der neue Spieltisch ist zerlegbar und transportabel. Die Übertragung der elektrischen Signale erfolgt per Lichtwellenleiter. In der Kirche sind mehrere Anschlußstellen vorhanden. So kann die Orgel nun von fast allen Stellen innerhalb der Kirche gespielt werden. Der Generalspieltisch ist auch für den Anschluß der 1965 von der Firma Führer (Wilhelmshaven) erbauten Chororgel mit 11 Registern auf zwei Manualen und Pedal vorbereitet.

Eddy Ottes (Barcelona) hat die Orgel neu intoniert. Sie ist gleichschwebend temperiert gestimmt (a' 440 Hz bei 18°C).

Die Orgel hat 3288 Pfeifen. 
Der tiefste Ton mit 16,35 Hz ist »C« (das große C) im Register »Untersatz 32'«, der höchste Ton mit 12,54 kHz ist »g'''« (das dreigestrichene g) im Register »Oktave 1'«. 

## Chororgel

<div class="zoomable" style="max-width: 400px">
<img class="r" src="/img/chororgel.jpg">
</div>



Diese Orgel stammt aus einer 1995 entwidmeten und zur Lagerhalle umgebauten Kirche der Stadt Tourcoing im Norden Frankreichs. Sie ist das größte Instrument dieses Orgelbauers auf deutschem Boden.
Aristide Cavaillé Coll ist weltweit einer der bedeutendsten Orgelbauer gewesen. Die meisten seiner großen Instrumente in den Kathedralen Frankreichs sind erhalten und stehen fast alle unter Denkmalschutz. Charles Mutin war der Compagnon von A.Cavaillé Coll und führte diese Firma bis zu ihrer Auflösung in den 1930igern ganz im Sinne ihres Gründers weiter.

Die Chororgel der St.-Nikolai-Kirche ist ein repräsentatives Beispiel für die Orgelbaukunst Frankreichs im 19.Jahrhdt. Sie besitzt einen voll-ständig erhaltenen, mechanischen, „seitenspieligen“ Spielschrank. Außerdem ist sie zusätzlich mit dem Spieltisch der Hauptorgel elektrisch verbunden. Dadurch können beide Orgeln gleichzeitig gespielt werden. Die Vorrichtungen der elektrischen Doppeltraktur sind im denkmal-pflegerischen Sinn eingebaut. Sie können mit wenigen Handgriffen wieder entfernt werden.

## Truhenorgel

<div class="zoomable" style="max-width: 400px">
<img class="r" src="/img/Truhenorgel_01.jpg">
</div>

Die Truhenorgel wurde speziell für die Anforderungen an St. Nikolai konstruiert und gefertigt: fundamentaler Klang, transponierbar einen Halbton tiefer und höher, Kammerton bis 430 Hertz, Möglichkeit zum Legen verschiedenster Temperaturen, leichte Bauweise, zwei Register (Gedackt 8‘ und Rohrflöte 4‘), Tonumfang C bis f‘‘‘.

Im Jahr 2002 wurde dieses Instrument aus der Gettorfer Orgelbauwerkstatt Babel vom Orgelbauverein St. Nikolai e.V. der Kirchengemeinde übergeben.


