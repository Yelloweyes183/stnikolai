Der __SanktNikolaiChor__ wurde 1922 gegründet und besteht derzeit aus rund 50 motivierten und engagierten Sängerinnen und Sängern, die auf hohem Niveau die Doppelaufgabe eines Konzert- und Gemeindechores erfüllen.

Sein Repertoire ist weit gespannt: Neben gottesdienstlicher a cappella- und orgelbegleiteter Musik zu den Hochfesten des Kirchenjahres singt der Chor in Bach-Kantaten-Gottesdiensten und Evensongs der Nikolai-Kirche. Die schlanke Besetzung ermöglicht in ihrer Flexibilität hochkarätige Projekte. Fast alle Oratorien vom Barock bis in die Moderne gehören zu seinem Repertoire, dessen größere Besetzungen gemeinsam mit befreundeten Chören realisiert werden.

Über die Grenzen von Schleswig-Holstein hinaus zählt der SanktNikolaiChor zu den führenden Oratorienchören Deutschlands. Er war Preisträger beim Schleswig-Holsteinischen Chorwettbewerb und auf seinen Chorreisen auch im Ausland (Italien, Schweiz, Frankreich, Polen und England) unterwegs.

2001, 2003 und 2012 gestaltete unser Ensemble unter Leitung von KMD Rainer-Michael Munz an den englischen Kathedralen in Bristol und York jeweils eine Woche lang die anspruchsvollen täglichen Evensongs und Gottesdienste.

Seit 2013 leitet KMD Volkmar Zehner den SanktNikolaiChor. In den ersten zwei gemeinsamen Jahren wurde die erfolgreiche musikalische Arbeit in diversen Gottesdiensten und Konzerten in Kiel und Umgebung mit neuen Akzenten fortgeführt.

2015 führte den Chor eine Chorreise in gotische Backstein-Kathedralen an der Ostsee (Wismar, Rostock, Greifswald und Lübeck), im Gepäck Motetten von J.S. Bach, Nystedt und Mendelssohn Bartholdy.

2016 hat der SanktNikolaiChor nach der Aufführung der Bachschen Johannes-Passion das Jubiläumsjahr in Kiel zum 100. Todestag von Max Reger auf vielfältige Art und Weise mitgestaltet: im „Reger-Festival-Chor“ der Philharmonischen Konzerte im Mai mit „Die Nonnen“ op. 112, im Juli bei dem Open-Air Konzert auf der Kieler Krusenkoppel und im November gemeinsam mit dem Hamburger Bach-Chor St. Petri und der Hamburger Camerata mit zwei vielbeachteten Aufführungen des „100. Psalms“ in der Nikolai-Kirche und der Hamburger Laeiszhalle.

2017 gestaltete der Chor das Reformationsjubiläum abwechslungsreich mit: zwei Festkonzerte mit Bachs „Oster“- und „Himmelfahrts-Oratorium“ sowie der Sinfonie Nr. 2 „Lobgesang“ von Felix Mendelssohn Bartholdy und Rainer-Michael Munz‘ „Te Deum“ waren neben einer weiteren Chorreise, diesmal nach Dresden und Umgebung mit Auftritten u.a. in der Dresdner Frauenkirche und im Dom zu Bautzen, die Höhepunkte des Jahres.

2018 standen die „Matthäus-Passion“ und das „Magnificat“ von J.S. Bach auf dem Programm.

Im Februar 2019 sang der Chor das „Requiem“ von W.A. Mozart sowie dessen Motette „Ave verum corpus“ auf Einladung der Musikfreunde Kiel e.V. im 200. Mozart-Konzert in der St. Nikolai-Kirche. Weitere Höhepunkte des kirchenmusikalischen Lebens waren zwei a cappella-Chorkonzerte mit doppelchörigen Messen von Josef Rheinberger und Frank Martin im Dom zu Lübeck und in St. Nikolai im November sowie das „Weihnachts-Oratorium“ von J.S. Bach im Dezember 2019.

2020 stand eigentlich J.S. Bachs „Johannes-Passion“ auf dem Programm. Die Corona-Pandemie verhinderte aber eine Woche vor dem geplanten Termin die Aufführung. Rund 1 1/2 Jahre ist das Chor- und Konzertleben stark eingeschränkt bzw. zum Erliegen gekommen.
Seit dem Spätsommer 2021 proben die Mitglieder des SanktNikolaiChores wieder und haben verschiedene Programme in Gottesdiensten und Konzerten zur Aufführung gebracht.

